function _pad(n) { return String(n).padStart(2, '0'); }
function _ds(y, m, d) { return `${y}-${_pad(m)}-${_pad(d)}`; }

/**
 * 연도별 공휴일 Map 반환 { 'YYYY-MM-DD' → '공휴일명' }
 */
export function getHolidaysForYear(year) {
  const map = new Map();

  // 1. 고정 공휴일 + 대체공휴일 자동 계산
  for (const [mmdd, name] of Object.entries(FIXED)) {
    const [m, d] = mmdd.split('-').map(Number);
    const ds = _ds(year, m, d);
    map.set(ds, name);

    const dow = new Date(year, m - 1, d).getDay(); // 0=일, 6=토
    if (dow === 0) {
      // 일요일 → 다음 월요일 대체
      const sub = new Date(year, m - 1, d + 1);
      const subDs = _ds(sub.getFullYear(), sub.getMonth() + 1, sub.getDate());
      if (!map.has(subDs)) map.set(subDs, `${name} 대체공휴일`);
    } else if (dow === 6 && SAT_SUB.has(mmdd)) {
      // 토요일 + 대체 적용 대상 → 다음 월요일 대체
      const sub = new Date(year, m - 1, d + 2);
      const subDs = _ds(sub.getFullYear(), sub.getMonth() + 1, sub.getDate());
      if (!map.has(subDs)) map.set(subDs, `${name} 대체공휴일`);
    }
  }

  // 2. 변동 공휴일 (덮어쓰기 — 하드코딩이 우선)
  const prefix = String(year);
  for (const [ds, name] of Object.entries(VARIABLE)) {
    if (ds.startsWith(prefix)) map.set(ds, name);
  }

  return map;
}

/**
 * 특정 날짜의 공휴일 이름 반환 (없으면 null)
 */
export function getHoliday(dateStr) {
  const year = parseInt(dateStr.slice(0, 4));
  return getHolidaysForYear(year).get(dateStr) ?? null;
}
